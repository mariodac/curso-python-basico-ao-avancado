# O módulo os para interaçao com o sistema
# O módulo 'os' fornece funções para interagir com o sistema operacional.
# Por exemplo, o módulo os.path contém funções para trabalhar com caminhos de
# arquivo e a função os.listdir() pode ser usada para listar os arquivos em um
# diretório. O método os.system() permite executar comandos do sistema
# operacional a partir do seu código Python.
# Windows 10/11 (PowerShell), Linux, Mac = clear
# Windows 7/8 (cmd) = cls

import os

print('a' * 80)
print('a' * 80)
print('a' * 80)
os.system('cls')
os.system('echo Hello, world!')
print('a' * 80)
print('a' * 80)
print('a' * 80)
